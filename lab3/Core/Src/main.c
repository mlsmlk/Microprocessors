		  TS_DATA = HAL_ADC_GetValue(&hadc3);

		  RESOLUTION_V = hadc1.Init.Resolution;
		  RESOLUTION_TS = hadc3.Init.Resolution;

		  if (RESOLUTION_V != RESOLUTION_TS) {
			  TS_DATA = TS_DATA * pow(2.0, ((double) (RESOLUTION_TS - RESOLUTION_V) / 4));
		  }

